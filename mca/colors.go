// colorsBRS is a mapping of colors for minecraft block names using Brickadia's colorspace
var colorsBRS = make(map[string]color.RGBA)

// uncolored is used to keep track of block names that dont have a color associated with them yet
var uncolored = make(map[string]int)

// PreconvertColors creates a new set of colors using Brickadia's colorspace
func PreconvertColors() {
	for name, color := range colors {
		colorsBRS[name] = brs.ConvertColor(color)
	}
}

// PrintUncolored prints to console the names and frequencies of blocks without colors
func PrintUncolored() {
	if len(uncolored) == 0 {
		return
	}

	fmt.Println("\nDebug Info:")
	for block, freq := range uncolored {
		ansi.Print(ansi.Yellow, block)
		fmt.Print(" ")
		ansi.Println(ansi.BrightBlue, fmt.Sprint(freq))
	}
}

// colorBRS returns the Brickadia colorBRS of a block
func (b BlockState) colorBRS() (color.RGBA, bool) {
	color, exists := colorsBRS[b.Name]
	if !exists {
		uncolored[b.Name]++
	}
	return color, exists
}

func (b BlockState) color() (color.RGBA, bool) {
	color, exists := colors[b.Name]
	if !exists {
		uncolored[b.Name]++
	}
	return color, exists
}
